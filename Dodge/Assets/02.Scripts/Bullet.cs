using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Bullet : MonoBehaviour
{
    public float speed = 20f; //탄알 이동 속력
    private Rigidbody bulletRigidibody; //이동에 사용할 리지드바디 컴포넌트
    void Start()
    {   //게임오브젝트에서 Rigidbody컴포넌트를 찾아 bulletRigidbody에 할당
        bulletRigidibody = GetComponent<Rigidbody>();
        //리지드바디의 속도 = 앞쪽 방향 * 이동 속력
        bulletRigidibody.velocity = transform.forward * speed;

        //3초 뒤에 자신의 게임 오브젝트 파괴
        Destroy(gameObject, 10f);
    }
    //트리거 충돌 시 자동으로 실행되는 메소드
    private void OnTriggerEnter(Collider other)
    {   //충돌한 상대방 게임 오브젝트가 player태그를 가진 경우
        if(other.tag == "Player")
        {   //상대방 게임 오브젝트에서 PlayerController 컴포넌트 가져오기
            PlayerController playerController = other.GetComponent<PlayerController>();

            //상대방으로부터 PlayerController 컴포넌트를 가져오는데 성공했다면
            if(playerController != null) // <-  없으면 참조값 없다고 에러뜸 
            {   //상대방 PlayerController 컴포넌트의 Die() 메서드 실행
                playerController.Die();
            }
        }
        
    }
    void Update()
    {
        
    }
}
